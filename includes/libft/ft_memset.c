#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*data;

	i = 0;
	data = (char *)b;
	while (i < len)
	{
		data[i] = c;
		i++;
	}
	return (b);
}
